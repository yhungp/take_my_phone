export const generateDetailedStorage = (storagesValues) => {
  var apps = convertToGB(storagesValues['apps'])
  var photos = convertToGB(storagesValues['photos'])
  var videos = convertToGB(storagesValues['videos'])
  var audio = convertToGB(storagesValues['audio'])
  var downloads = convertToGB(storagesValues['downloads'])

  return {
    data: (canvas) => {
      let ctx = canvas.getContext("2d");

      let gradientStroke = ctx.createLinearGradient(0, 230, 0, 50);

      gradientStroke.addColorStop(1, "rgba(29,140,248,0.2)");
      gradientStroke.addColorStop(0.4, "rgba(29,140,248,0.0)");
      gradientStroke.addColorStop(0, "rgba(29,140,248,0)"); //blue colors

      return {
        labels: [
          "Apps",
          "Photos",
          "Videos",
          "Audio",
          "Downloads",
        ],
        datasets: [
          {
            label: "Used space (GB)",
            fill: true,
            backgroundColor: gradientStroke,
            borderColor: "#1f8ef1",
            borderWidth: 2,
            borderDash: [],
            borderDashOffset: 0.0,
            pointBackgroundColor: "#1f8ef1",
            pointBorderColor: "rgba(255,255,255,0)",
            pointHoverBackgroundColor: "#1f8ef1",
            pointBorderWidth: 20,
            pointHoverRadius: 4,
            pointHoverBorderWidth: 15,
            pointRadius: 4,
            data: [
              apps,
              photos,
              videos,
              audio,
              downloads]
          }
        ]
      };
    },
    options: storage_options
  }
}

function convertToGB(bytesValue) {
  let gbValue = (bytesValue / (1000 * 1000 * 1000)).toFixed(2);
  return gbValue
}
